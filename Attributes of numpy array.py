from numpy import*
a = array([101,102,103,104,105])
b = array([[10,20,30,40],[50,60,70,80]])
print()

# ndim attribute
print ("1D array nidm:",a.ndim)
print ("2D array nidm:",b.ndim)
print ()

#shape Attribute
print ("1D array shape:",a.shape)
print ("2D array shape:",b.shape)
print ()

#size Attribute
print ("1D array size:",a.size)
